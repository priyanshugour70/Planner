    /**
     * Get a random thought
     */
    fun getRandomThought(): String = thoughts.random()
    
    /**
     * Get thought of the day (based on day of year for consistency)
     */
    fun getThoughtOfTheDay(): String {
        val dayOfYear = java.util.Calendar.getInstance().get(java.util.Calendar.DAY_OF_YEAR)
        return thoughts[dayOfYear % thoughts.size]
    }
    
    /**
     * Get thought based on current hour (changes every hour)
     */
    fun getHourlyThought(): String {
        val calendar = java.util.Calendar.getInstance()
        val dayOfYear = calendar.get(java.util.Calendar.DAY_OF_YEAR)
        val hour = calendar.get(java.util.Calendar.HOUR_OF_DAY)
        val index = (dayOfYear * 24 + hour) % thoughts.size
        return thoughts[index]
    }
    
    /**
     * Get thoughts by category for targeted inspiration
     */
    fun getThoughtsByCategory(category: ThoughtCategory): List<String> {
        return when (category) {
            ThoughtCategory.SUCCESS -> thoughts.subList(0, 50)
            ThoughtCategory.DISCIPLINE -> thoughts.subList(50, 100)
            ThoughtCategory.HEALTH -> thoughts.subList(100, 150)
            ThoughtCategory.CAREER -> thoughts.subList(150, 200)
            ThoughtCategory.LEARNING -> thoughts.subList(200, 250)
            ThoughtCategory.MINDSET -> thoughts.subList(250, 300)
            ThoughtCategory.COMMUNICATION -> thoughts.subList(300, 350)
            ThoughtCategory.TIME_MANAGEMENT -> thoughts.subList(350, 400)
            ThoughtCategory.PERSISTENCE -> thoughts.subList(400, 450)
            ThoughtCategory.RELATIONSHIPS -> thoughts.subList(450, 500)
            ThoughtCategory.GENERAL -> thoughts.subList(500, thoughts.size)
        }
    }
    
    /**
     * Get a motivational message with user's name
     */
    fun getPersonalizedThought(firstName: String): String {
        val thought = getRandomThought()
        return if (firstName.isNotBlank()) {
            "$firstName, remember: $thought"
        } else {
            thought
        }
    }
    
